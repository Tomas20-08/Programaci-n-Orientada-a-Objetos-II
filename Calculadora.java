
package Taller_POO_Ejercicio03;

public class Calculadora {
 
    // METODO
    public int sumar(int a, int b) {
        int resultado = a + b;
        System.out.println(a + " + " + b + " = " + resultado);
        return resultado;
    }
 
    // METODO
    public int restar(int a, int b) {
        int resultado = a - b;
        System.out.println(a + " - " + b + " = " + resultado);
        return resultado;
    }
}

